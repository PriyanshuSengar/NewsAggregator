const day='monday';
// In switch case break is mandatory
switch(day){
    case 'sunday':
        console.log("Holiday!");
        break;
        case 'monday':
            console.log("working day");
            break;
        case 'tuesday':
            console.log("another working day");
            break;
        case 'wednesday':
            console.log("also a working day");
            break;
}
